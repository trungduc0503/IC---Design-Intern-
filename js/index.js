function toggleContentInfo() {
    const info = document.getElementById("content-info");
    const icon = document.getElementById("toggle-icon-content");
    if (info.style.display === "none" || info.style.display === "") {
      info.style.display = "block";
      icon.textContent = "expand_less"; // Thay đổi biểu tượng thành "thu gọn"
    } else {
      info.style.display = "none";
      icon.textContent = "expand_more"; // Thay đổi biểu tượng thành "mở rộng"
    }
  }
